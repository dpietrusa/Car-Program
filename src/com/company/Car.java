package com.company;

public class Car {

    private int year;
    private String make;
    private String color;

    public void setYear(int carYear) {
        year = carYear;
    }
    public int getYear() {
        return year;
    }


    public void setMake(String carMake) {
        make = carMake;
    }
    public String getMake() {
        return make;
    }


    public void setColor(String carColor) {
        color = carColor;
    }
    public String getColor() {
        return color;
    }





}
